    public void AnalizarArchivo(String contenido){
        contenido=contenido.replaceAll("<scrabble>","");
        contenido=contenido.replaceAll("</scrabble>", "");
        contenido=contenido.replaceAll("<dimension>", "");
        contenido=contenido.replaceAll("</dimension>","");;
        String[] contenido2=contenido.split("<dobles>");
        Tamanio=Integer.parseInt(contenido2[0]);
        contenido2=contenido2[1].split("</dobles>");
        contenido2[0]=contenido2[0].replaceAll("<casilla>", "");
        String[] contenido3=contenido2[0].split("</casilla>");
        String contenido4;
        String[] contenido5;
        for(int i = 0; i<contenido3.length;i++){
            contenido4=contenido3[i].replaceAll("<x>", "");
            contenido4=contenido4.replaceAll("<y>", "");
            contenido4=contenido4.replaceAll("</y>", "");
            contenido5=contenido4.split("</x>");
            Dobles.InsertarCasilla(Integer.parseInt(contenido5[0]), Integer.parseInt(contenido5[1]));
        }
        String contenido6=contenido2[1].replaceAll("<triples>", "");
        String[] contenido7=contenido6.split("</triples>");
        contenido7[0]=contenido7[0].replaceAll("<casilla>","");
        String[] contenido8=contenido7[0].split("</casilla>");
        String contenido9;
        String[] contenido10;
        for(int i = 0; i<contenido8.length;i++){
            contenido9=contenido8[i].replaceAll("<x>", "");
            contenido9=contenido9.replaceAll("<y>", "");
            contenido9=contenido9.replaceAll("</y>", "");
            contenido10=contenido9.split("</x>");
            Triples.InsertarCasilla(Integer.parseInt(contenido10[0]), Integer.parseInt(contenido10[1]));
        }
        contenido7[1]=contenido7[1].replaceAll("<diccionario>", "");
        contenido7[1]=contenido7[1].replaceAll("</diccionario>", "");
        contenido7[1]=contenido7[1].replaceAll("<palabra>", "");
        String[] contenido11=contenido7[1].split("</palabra>");
        for(int i=0;i<contenido11.length;i++){
            Palabras.InsertarPalabra(contenido11[i]);
        }
        Dobles.RecorrerCasillas();
        Triples.RecorrerCasillas();
        Palabras.mostrarPalabras();
    }
